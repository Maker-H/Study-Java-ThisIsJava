public class Ch4_Exercise6_StarTriangle{
    public static void main(String[] args){
     int num=1;
     for(int i=1;i<=5;i++) {
        for(int j=1;j<=num;j++) {
          System.out.print(("*"));
        }
        System.out.println();
        num++;
     }
     
    }//end of main    
}//end of class